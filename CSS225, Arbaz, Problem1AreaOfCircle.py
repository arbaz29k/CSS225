# Problem number 1 - A function areaOfCircle(r) which returns the area of a circle of radius

def areaOfCircle(r):
    from math import pi  #This will get the value of pi from math module.
    return pi * r * r  #This returns the area of circle which is calculated as pi times r squared
print(areaOfCircle(1))  #This prints the areaofcircle